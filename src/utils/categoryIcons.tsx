// ── Fast lookup map ────────────────────────────────────────────────────
const ICON_MAP = new Map<string, CatIconDef>(
  CATEGORY_ICONS.map(i => [i.key, i])
);

// ── Background colour for legacy emoji/name-based icons ───────────────
const CAT_COLORS = [
  '#e05850','#e07830','#c9a030','#50a860','#4090c8',
  '#8060c0','#d04080','#30a0a0','#805040','#4060a0',
];
function hashColor(name: string): string {
  let h = 0;
  for (let i = 0; i < name.length; i++) h = name.charCodeAt(i) + ((h << 5) - h);
  return CAT_COLORS[Math.abs(h) % CAT_COLORS.length];
}

// ── Renderer ──────────────────────────────────────────────────────────
// Renders a circular category icon (SVG key OR legacy emoji).
export function renderCategoryIcon(
  icon: string | undefined,
  name: string,
  size = 40
): JSX.Element {
  const iconDef = icon ? ICON_MAP.get(icon) : undefined;

  if (iconDef) {
    return (
      <div
        className="rounded-full flex items-center justify-center flex-shrink-0"
        style={{ width: size, height: size, backgroundColor: iconDef.bg }}
      >
        <svg
          viewBox="0 0 24 24"
          style={{ width: size * 0.56, height: size * 0.56 }}
          fill="white"
        >
          {Array.isArray(iconDef.d)
            ? iconDef.d.map((d, i) => <path key={i} d={d} />)
            : <path d={iconDef.d} />}
        </svg>
      </div>
    );
  }

  // Legacy emoji or empty — coloured circle with text
  return (
    <div
      className="rounded-full flex items-center justify-center flex-shrink-0"
      style={{ width: size, height: size, backgroundColor: hashColor(name), fontSize: size * 0.4 }}
    >
      {icon || (name ? name[0].toUpperCase() : '?')}
    </div>
  );
}
